#[test]
fn finder_patterns_land_in_the_corners_05() {
	let qr = match encode("finder", QrEcc::Medium) {
		Ok(q) => q,
		Err(e) => panic!("encode failed: {:?}", e),
	};
	let n = qr.size();
	// Each finder is a 7 by 7 block: a dark ring, a light ring, then a dark 3 by 3 core.
	for &(ox, oy) in &[(0usize, 0usize), (n - 7, 0), (0, n - 7)] {
		for dy in 0 .. 7 {
			for dx in 0 .. 7 {
				let ring = dx == 0 || dx == 6 || dy == 0 || dy == 6;
				let core = (2 ..= 4).contains(&dx) && (2 ..= 4).contains(&dy);
				let expect = ring || core;
				assert_eq!(
					qr.get(ox + dx, oy + dy), expect,
					"finder at ({},{}) cell ({},{})", ox, oy, dx, dy,
				);
			}
		}
	}
}

#[test]
fn timing_patterns_alternate_06() {
	let qr = match encode("timing", QrEcc::Medium) {
		Ok(q) => q,
		Err(e) => panic!("encode failed: {:?}", e),
	};
	let n = qr.size();
	// Row six and column six alternate dark and light between the finder separators.
	for i in 8 .. n - 8 {
		let expect = i % 2 == 0;
		assert_eq!(qr.get(i, 6), expect, "horizontal timing at {}", i);
		assert_eq!(qr.get(6, i), expect, "vertical timing at {}", i);
	}
}

#[test]
fn version_grows_with_the_payload_07() {
	// The side length is version * 4 + 17, and a longer payload forces a larger version.
	let small = match encode("a", QrEcc::Low) {
		Ok(q) => q,
		Err(e) => panic!("encode failed: {:?}", e),
	};
	assert_eq!(small.version(), 1);
	assert_eq!(small.size(), 21);

	let mut last = 0u8;
	for len in [10usize, 100, 400, 1000, 2000] {
		let text = "z".repeat(len);
		let qr = match encode(&text, QrEcc::Low) {
			Ok(q) => q,
			Err(e) => panic!("encode failed at len {}: {:?}", len, e),
		};
		assert!(qr.version() >= last, "version should not shrink as data grows");
		assert_eq!(qr.size(), qr.version() as usize * 4 + 17);
		last = qr.version();
	}
}

#[test]
fn overlong_payload_is_refused_08() {
	// Version 40 at High holds well under 3000 data bytes, so this cannot fit.
	let text = "q".repeat(5000);
	assert!(encode(&text, QrEcc::High).is_err());
}

#[test]
fn forced_mask_and_version_are_honoured_09() {
	// The advanced entry point can pin a version and mask; the size must follow the version.
	let qr = match encode_bytes_advanced(b"pinned", QrEcc::Low, 5, 5, Some(3), false) {
		Ok(q) => q,
		Err(e) => panic!("encode failed: {:?}", e),
	};
	assert_eq!(qr.version(), 5);
	assert_eq!(qr.size(), 37);
	assert_eq!(qr.ecc(), QrEcc::Low); // Boosting is off, so the level is exactly as asked.
}

#[test]
fn bad_arguments_are_rejected_10() {
	assert!(encode_bytes_advanced(b"x", QrEcc::Low, 0, 40, None, true).is_err());
	assert!(encode_bytes_advanced(b"x", QrEcc::Low, 1, 41, None, true).is_err());
	assert!(encode_bytes_advanced(b"x", QrEcc::Low, 10, 5, None, true).is_err());
	assert!(encode_bytes_advanced(b"x", QrEcc::Low, 1, 40, Some(8), true).is_err());
}

