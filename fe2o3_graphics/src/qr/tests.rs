//! Correctness tests for the QR encoder.
//!
//! The golden matrices below were produced by Project Nayuki's own `qrcodegen` crate (version 1.8)
//! via its `QrCode::encode_binary`, which forces byte mode and, like this encoder, boosts the
//! error-correction level to fill spare capacity. They serve as an external oracle: a matching
//! grid means the whole pipeline (segment packing, Reed-Solomon, block interleaving, function
//! patterns, and mask selection) agrees with an independent, widely used implementation of the
//! same frozen standard, not merely with itself.

use super::*;

/// Renders a matrix as rows of `0` and `1` so it can be compared against a golden literal.
fn rows_of(qr: &QrMatrix) -> String {
	let mut s = String::new();
	for y in 0 .. qr.size() {
		for x in 0 .. qr.size() {
			s.push(if qr.get(x, y) { '1' } else { '0' });
		}
		if y + 1 < qr.size() {
			s.push('\n');
		}
	}
	s
}

/// Encodes text in byte mode and checks the version, side length, and every module against a
/// golden matrix from the oracle.
fn check(text: &str, ecc: QrEcc, ver: u8, size: usize, golden: &str) {
	let qr = match encode(text, ecc) {
		Ok(q) => q,
		Err(e) => panic!("encode failed: {:?}", e),
	};
	assert_eq!(qr.version(), ver, "version mismatch");
	assert_eq!(qr.size(), size, "size mismatch");
	assert_eq!(rows_of(&qr), golden.trim(), "matrix mismatch");
}

